package Literal;
public class Literal {
    public static void main(String args[]){
        int num1=0b101;
        System.out.println(num1);

        int num2=0x7E;
        System.out.println(num2);

        char c='k';
        c++;
        System.out.println(c);

        int num3=10_00_000;
        System.out.println(num3);


    }
}
